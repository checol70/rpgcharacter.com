function Ancestry (name, hitPoints, size, speed, boosts, flaw, traits, specialAbilities){
    this.hitPoints = hitPoints;
    this.size = size;
    this.speed = speed;
    this.flaw = flaw;
    this.traits = traits;
    this.specialAbilities = specialAbilities;
    this.name = name;
    this.boosts = boosts;
}
export{Ancestry}